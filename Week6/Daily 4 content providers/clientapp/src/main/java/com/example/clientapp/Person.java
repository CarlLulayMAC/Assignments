package com.example.clientapp;

class Person {
    String name;
    int age;
    String city;
    String email;
    String phone;

    public Person(String name, int age, String city, String email, String phone) {
        this.name = name;
        this.age = age;
        this.city = city;
        this.email = email;
        this.phone = phone;
    }
}
